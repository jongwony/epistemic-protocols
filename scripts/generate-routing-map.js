#!/usr/bin/env node
/**
 * Generate the agent-facing session routing map from canonical sources.
 *
 * The harness already holds every skill's `description:` frontmatter, yet a
 * passive capability description does not trigger invocation. This map is an
 * active, deficit-framed routing DIRECTIVE meant to be injected at SessionStart
 * (see epistemic-cooperative/skills/catalog/scripts/session-context.js). It is
 * generated 100% from canonical sources — no hand-written gloss:
 *
 *   1. deficit-framed `when:` trigger = the "When to Use" column of the catalog
 *      table in epistemic-cooperative/skills/catalog/SKILL.md.
 *   2. `Deficit → Resolution` spine + Name/command = scripts/load-protocols.js
 *      records (discoverPlugins().filter(p => p.isProtocol)).
 *
 * The `description:` frontmatter gloss is intentionally NOT used (redundant with
 * what the harness already carries).
 *
 * Usage:
 *   node scripts/generate-routing-map.js            # write routing-map.md
 *   node scripts/generate-routing-map.js --check     # exit non-zero if stale
 *
 * Zero external dependencies: Node.js standard library only.
 */

const fs = require('fs');
const path = require('path');

const DEFAULT_PROJECT_ROOT = path.resolve(__dirname, '..');

const CATALOG_SKILL_REL = 'epistemic-cooperative/skills/catalog/SKILL.md';
const ROUTING_MAP_REL = 'epistemic-cooperative/skills/catalog/routing-map.md';

// Top-line marker: this artifact is generated, never hand-edited. Kept as an
// HTML comment so it renders invisibly wherever the map is displayed while
// still marking provenance in the raw file (and in the injected context).
const AUTOGEN_COMMENT =
  '<!-- AUTO-GENERATED by scripts/generate-routing-map.js from ' +
  'epistemic-cooperative/skills/catalog/SKILL.md (When-to-Use triggers) + ' +
  'scripts/load-protocols.js (Deficit → Resolution). Do not edit by hand; ' +
  'regenerate with: node scripts/generate-routing-map.js -->';

// Active routing directive. Descriptions alone under-trigger invocation, so the
// preamble instructs the agent to route from the deficit, not the summary.
const PREAMBLE =
  '# Epistemic Protocols — session routing map (auto-generated; do not edit by hand)\n\n' +
  'These protocols each resolve a specific interaction deficit. On each user turn, if the\n' +
  'situation matches a `when:` trigger below, proactively invoke (or offer) that protocol —\n' +
  'the skill descriptions alone under-trigger invocation. Route from the deficit, not the summary.\n';

// Catalog table row: | Name | `/command` | When to Use |. Name is the protocol
// display name (capitalized), command is all-lowercase, third cell is the
// deficit-framed trigger. Header row (When to Use) is filtered by the caller.
const CATALOG_ROW_RE = /^\|\s*([A-Z][A-Za-z]+)\s*\|\s*`\/([a-z]+)`\s*\|\s*(.+?)\s*\|\s*$/;
const CLUSTER_HEADING_RE = /^###\s+(.+?)\s*$/;

/**
 * Parse the catalog SKILL.md When-to-Use table into ordered routing entries.
 * Insertion order == catalog table order == presentation order.
 *
 * @returns {{ order: string[], triggers: Map, clusterOf: Map }}
 *   order: `/command` strings in catalog order
 *   triggers: `/command` → { name, trigger }
 *   clusterOf: `/command` → cluster name
 */
function parseCatalog(catalogText) {
  const triggers = new Map();
  const clusterOf = new Map();
  const order = [];
  let cluster = null;

  for (const line of catalogText.split('\n')) {
    const h = line.match(CLUSTER_HEADING_RE);
    if (h) { cluster = h[1]; continue; }
    const m = line.match(CATALOG_ROW_RE);
    if (m && m[3] !== 'When to Use') {
      const cmd = '/' + m[2];
      if (!triggers.has(cmd)) order.push(cmd);
      triggers.set(cmd, { name: m[1], trigger: m[3] });
      clusterOf.set(cmd, cluster);
    }
  }

  return { order, triggers, clusterOf };
}

/**
 * Build the structured routing model from both canonical sources.
 *
 * Robustness: FAIL LOUDLY, bidirectionally, on any catalog/protocol drift —
 * silently dropping or stale-carrying an entry would go unnoticed otherwise.
 * (a) a protocol discovered by load-protocols with no matching catalog row,
 * (b) a catalog row whose command is not a discovered protocol (e.g. the
 * protocol was removed/renamed and its old row was left behind — without this
 * guard the stale row would still render and regenerate byte-identically,
 * routing-map-sync would pass, and SessionStart would inject a directive for
 * a non-existent protocol). A null deficit/resolution (Type signature absent)
 * omits the spine gracefully rather than emitting the literal "null".
 *
 * @returns {Array<{ cmd, name, cluster, deficit, resolution, trigger }>}
 */
function buildRoutingEntries(options = {}) {
  // Resolve to absolute: a relative projectRoot (e.g. "." from the verifier)
  // would make the require() below a bare module identifier, not a file path.
  const projectRoot = path.resolve(options.projectRoot || DEFAULT_PROJECT_ROOT);
  const { discoverPlugins } = require(path.join(projectRoot, 'scripts', 'load-protocols.js'));

  const catalogText = fs.readFileSync(path.join(projectRoot, CATALOG_SKILL_REL), 'utf8');
  const { order, triggers, clusterOf } = parseCatalog(catalogText);

  const spine = new Map();
  for (const r of discoverPlugins({ projectRoot }).filter(p => p.isProtocol)) {
    spine.set('/' + r.skill, { deficit: r.deficit, resolution: r.resolution });
  }

  // Drift guard (a): every protocol must have a catalog row.
  const missingRows = [...spine.keys()].filter(cmd => !triggers.has(cmd));
  if (missingRows.length) {
    throw new Error(
      `[generate-routing-map] ${missingRows.length} protocol(s) missing from the catalog ` +
      `When-to-Use table (${CATALOG_SKILL_REL}): ${missingRows.join(', ')}. ` +
      `Add a catalog row for each, or the routing map would silently drop them.`
    );
  }

  // Drift guard (b): every catalog row must correspond to a discovered protocol
  // (reject stale rows left behind by a removed/renamed protocol).
  const staleRows = order.filter(cmd => !spine.has(cmd));
  if (staleRows.length) {
    throw new Error(
      `[generate-routing-map] ${staleRows.length} catalog row(s) reference protocol(s) not ` +
      `discovered by load-protocols.js: ${staleRows.join(', ')}. ` +
      `Remove the stale catalog row(s), or the routing map would route to a non-existent protocol.`
    );
  }

  return order.map(cmd => {
    const t = triggers.get(cmd);
    const s = spine.get(cmd) || {};
    return {
      cmd,
      name: t.name,
      cluster: clusterOf.get(cmd),
      // (b) null deficit/resolution → omit the spine gracefully downstream.
      deficit: s.deficit || null,
      resolution: s.resolution || null,
      trigger: t.trigger,
    };
  });
}

/** Render the routing entries into the map text (grouped by cluster). */
function renderRoutingMap(entries) {
  let out = AUTOGEN_COMMENT + '\n' + PREAMBLE;
  let lastCluster = null;
  for (const e of entries) {
    if (e.cluster !== lastCluster) {
      out += `\n## ${e.cluster}\n`;
      lastCluster = e.cluster;
    }
    const arrow = e.deficit && e.resolution ? ` — ${e.deficit} → ${e.resolution}` : '';
    out += `\n**\`${e.cmd}\`** (${e.name})${arrow}\n`;
    out += `  when: ${e.trigger}\n`;
  }
  return out;
}

/** Full pipeline: canonical sources → routing map text. */
function generateRoutingMap(options = {}) {
  return renderRoutingMap(buildRoutingEntries(options));
}

function writeRoutingMap(options = {}) {
  const projectRoot = path.resolve(options.projectRoot || DEFAULT_PROJECT_ROOT);
  const text = generateRoutingMap(options);
  const outPath = path.join(projectRoot, ROUTING_MAP_REL);
  fs.writeFileSync(outPath, text, 'utf8');
  return { outPath, text };
}

/**
 * Compare the committed routing map against a fresh generation.
 * @returns {{ inSync: boolean, reason?: string }}
 */
function checkRoutingMap(options = {}) {
  const projectRoot = path.resolve(options.projectRoot || DEFAULT_PROJECT_ROOT);
  const generated = generateRoutingMap(options);
  const outPath = path.join(projectRoot, ROUTING_MAP_REL);
  if (!fs.existsSync(outPath)) {
    return { inSync: false, reason: `committed routing map not found at ${ROUTING_MAP_REL}` };
  }
  const committed = fs.readFileSync(outPath, 'utf8');
  if (committed !== generated) {
    return { inSync: false, reason: `committed ${ROUTING_MAP_REL} differs from freshly generated output` };
  }
  return { inSync: true };
}

module.exports = {
  CATALOG_SKILL_REL,
  ROUTING_MAP_REL,
  AUTOGEN_COMMENT,
  PREAMBLE,
  parseCatalog,
  buildRoutingEntries,
  renderRoutingMap,
  generateRoutingMap,
  writeRoutingMap,
  checkRoutingMap,
};

if (require.main === module) {
  const args = process.argv.slice(2);
  const projectRoot = process.cwd();
  try {
    if (args.includes('--check')) {
      const { inSync, reason } = checkRoutingMap({ projectRoot });
      if (!inSync) {
        process.stderr.write(
          `[generate-routing-map] STALE: ${reason}. ` +
          `Regenerate with: node scripts/generate-routing-map.js\n`
        );
        process.exit(1);
      }
      process.stderr.write('[generate-routing-map] routing-map.md is in sync with canonical sources\n');
    } else {
      const { outPath, text } = writeRoutingMap({ projectRoot });
      const count = (text.match(/^\*\*`\//gm) || []).length;
      process.stderr.write(`[generate-routing-map] wrote ${count} protocols → ${path.relative(projectRoot, outPath)}\n`);
    }
  } catch (e) {
    process.stderr.write(`[generate-routing-map] ERROR: ${e.message}\n`);
    process.exit(2);
  }
}
